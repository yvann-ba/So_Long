#include "libft.h"

void	*ft_memchr(const void *s, int c, unsigned long n)
{
	unsigned long	i;

	i = 0;
	while (i < n)
	{
		if (((unsigned char *)s)[i] == (unsigned char)c)
		{
			return ((void *)(unsigned char *)s + i);
		}
		i++;
	}
	return (NULL);
}
